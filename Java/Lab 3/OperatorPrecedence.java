//10+20*30
//100/10*100
//5*4/4%3
//100+200/10-3*10
public class OperatorPrecedence{
    public static void main(String[] args){
        System.out.println("double d:"+(10+20*30));
        System.out.println("double d:"+(100/10*100));
        System.out.println("double d:"+(5*4/4%3));
        System.out.println("double d:"+(100+200/10-3*10));
    }
}